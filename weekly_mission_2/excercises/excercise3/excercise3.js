
class Twitter {
    constructor(name, user, follow, followers){
      this.name = name
      this.user = user
      this.follow = follow
      this.followers = followers
    }
}

class Facebook {
    constructor(user, friends, followers){
        this.user = user
        this.friends = friends
        this.followers = followers
    }
}

class Uber {
    constructor(user, stars){
        this.user = user
        this.stars = stars
    }
}